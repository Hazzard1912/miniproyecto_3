    private void obtenerHora(){
        
        int anio = Integer.parseInt(jComboAnio.getItemAt(opcion1));
        int mes = (Integer.parseInt(jComboMes.getItemAt(opcion2))) - 1;
        int dia = Integer.parseInt(jComboDia.getItemAt(opcion3));
        int hora = Integer.parseInt(jComboHora.getItemAt(opcion4));
        int minutos = Integer.parseInt(jComboMinutos.getItemAt(opcion5));
        
        try {
            cal.set(anio, mes, dia, hora, minutos);
            System.out.println("Fecha asignada con exito");
        } catch (Exception e) {
            System.out.println(e.getMessage());
            System.out.println("alguno o mas datos ingresados son incorrectos, verifique");
        }
    }
    
